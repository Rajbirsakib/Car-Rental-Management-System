    private void CustomerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CustomerTableMouseClicked
       
        DefaultTableModel model= (DefaultTableModel)CustomerTable.getModel();
        int MyIndex= CustomerTable.getSelectedRow();
        CustIDTb.setText(model.getValueAt(MyIndex, 0).toString());
        CustNameTb.setText(model.getValueAt(MyIndex, 1).toString());
        CustAddTb.setText(model.getValueAt(MyIndex, 2).toString());
        CustPhnTb.setText(model.getValueAt(MyIndex, 3).toString());
        CustNIDTb.setText(model.getValueAt(MyIndex, 4).toString());
    }//GEN-LAST:event_CustomerTableMouseClicked

    private void EditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBtnActionPerformed
        
        if(CustIDTb.getText().isEmpty() || CustNameTb.getText().isEmpty() || CustAddTb.getText().isEmpty() || CustPhnTb.getText().isEmpty() || CustNIDTb.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Select a Customer For Updated...");
        }
        else{
        try{
            Con= DriverManager.getConnection("jdbc:derby://localhost:1527/CarRental","Admin00","12345");
            String CustId= CustIDTb.getText();
            String Query= "Update admin00.customerstable set customer_name='"+CustNameTb.getText()+"',Address='"+CustAddTb.getText()+"',Phone_no='"+CustPhnTb.getText()+"',NID_No='"+CustNIDTb.getText()+"' where customer_id="+CustId;
            Statement Add= Con.createStatement();
            Add.executeUpdate(Query);
            JOptionPane.showMessageDialog(this, "Customer Information Updated Successfully...");
            DisplayCars();
            Reset();
        }
        catch(Exception e){
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_EditBtnActionPerformed

