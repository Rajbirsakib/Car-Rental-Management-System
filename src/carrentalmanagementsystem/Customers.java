    private void DisplayCars(){
        try{
            Con= DriverManager.getConnection("jdbc:derby://localhost:1527/CarRental","Admin00","12345");
            St= Con.createStatement();
            Rs= St.executeQuery("select * from CustomersTable");
            CustomerTable.setModel(DbUtils.resultSetToTableModel(Rs));
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }
    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
      
        if(CustIDTb.getText().isEmpty() || CustNameTb.getText().isEmpty() || CustAddTb.getText().isEmpty() || CustPhnTb.getText().isEmpty() || CustNIDTb.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Information...");
        }
        else{
        try{
            Con= DriverManager.getConnection("jdbc:derby://localhost:1527/CarRental","Admin00","12345");
            PreparedStatement add= Con.prepareStatement("insert into CustomersTable values(?,?,?,?,?)");
            add.setString(1, CustIDTb.getText());
            add.setString(2, CustNameTb.getText());
            add.setString(3, CustAddTb.getText());
            add.setString(4, CustPhnTb.getText());
            add.setString(5, CustNIDTb.getText());
            int row=add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Customer Information Added Successfully...");
            DisplayCars();
        }
        catch(Exception e){
            e.printStackTrace();
        }
        }
    }//GEN-LAST:event_SaveBtnActionPerformed

